// --- Internal State for Text Wrapping ---
static uint8_t current_x = 0;
static uint8_t current_page = 0;

void sh1106_command(uint8_t cmd) {
    i2c_start();
    i2c_write(SH1106_I2C_ADDR);
    i2c_write(0x00); // Command mode
    i2c_write(cmd);
    i2c_stop();
}

void sh1106_data(uint8_t data) {
    i2c_start();
    i2c_write(SH1106_I2C_ADDR);
    i2c_write(0x40); // Data mode
    i2c_write(data);
    i2c_stop();
}

void sh1106_init(void) {
    sh1106_command(0xAE); // Display OFF
    sh1106_command(0xA1); // Set Segment Re-map
    sh1106_command(0xC8); // Set COM Output Scan Direction
    sh1106_command(0xAD); // DC-DC Control Mode Set (Charge pump)
    sh1106_command(0x8B); // DC-DC ON (SH1106 specific)
    sh1106_command(0xAF); // Display ON
}

void sh1106_clear(void) {
    for (uint8_t page = 0; page < 8; page++) {
        sh1106_command(0xB0 + page); // Set Page address
        
        // Setup column address taking into account the SH1106 2-pixel offset
        sh1106_command(0x00 | (SH1106_OFFSET & 0x0F));        // Set Lower Column
        sh1106_command(0x10 | ((SH1106_OFFSET >> 4) & 0x0F)); // Set Higher Column

        i2c_start();
        i2c_write(SH1106_I2C_ADDR);
        i2c_write(0x40); // Data stream follows
        for (uint8_t col = 0; col < 128; col++) {
            i2c_write(0x00); // Write blank pixel
        }
        i2c_stop();
    }
    
    // Reset cursor state to top-left after clearing
    current_x = 0;
    current_page = 0;
}

void sh1106_set_cursor(uint8_t x, uint8_t page) {
    // Save the internal state
    current_x = x;
    current_page = page;

    // Add the 2-pixel offset to the requested X coordinate
    uint8_t physical_x = x + SH1106_OFFSET; 
    
    sh1106_command(0xB0 + page);                          // Set page address
    sh1106_command(0x00 | (physical_x & 0x0F));           // Set lower column address
    sh1106_command(0x10 | ((physical_x >> 4) & 0x0F));    // Set higher column address
}

void sh1106_print_char(char c) {
    // 1. Handle explicit newline characters
    if (c == '\n') {
        current_x = 0;
        current_page++;
        if (current_page > 7) current_page = 0; // Wrap back to top if we run off the bottom
        sh1106_set_cursor(current_x, current_page);
        return;
    }

    // 2. Check for automatic line wrap (if next char exceeds 128px width)
    if (current_x + 6 > 128) {
        current_x = 0;
        current_page++;
        if (current_page > 7) current_page = 0;
        sh1106_set_cursor(current_x, current_page);
    }

    // 3. Print the character
    if (c < 32 || c > 126) c = 32; // Filter unsupported characters
    
    for (uint8_t i = 0; i < 5; i++) {
        sh1106_data(pgm_read_byte(&font5x8[c - 32][i]));
    }
    sh1106_data(0x00); // 1 pixel spacer

    // 4. Advance the internal cursor tracker by 6 pixels
    current_x += 6;
}

void sh1106_print_string(const char *str) {
    while (*str) {
        sh1106_print_char(*str++);
    }
}